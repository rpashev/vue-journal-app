        <h1>Reflective Journal</h1>
        <p>
          A reflective journal (aka a reflective diary) is the perfect place to jot down some of
          life's biggest thoughts. In a reflective journal, you can write about a positive or
          negative event that you experienced, what it means or meant to you, and what you may have
          learned from that experience.
        </p>
        <h2>5 Reasons To Write a Reflective Journal</h2>
        <p>
          Here are some of the most common reasons why people find reflective journals so useful:
        </p>
        <ol>
          <li>
            <p>
              <strong>To make sense of things that happened.</strong> What you write should sound as
              if you are describing the details to someone who wasn't there. Be as descriptive as
              possible. Just the act of writing down the details of what happened may give you
              perspective that you may not have otherwise considered had you just continued to think
              about it.
            </p>
          </li>
          <li>
            <p>
              <strong>To speculate as to why something is the way it is.</strong>
              Your views can come from your own common sense, or from something you have heard at a
              lecture or read in a book. Either way, speculating why something is the way it is can
              be a very useful exercise in reasoning.
            </p>
          </li>
          <li>
            <p>
              <strong>To align future actions with your reflected values and experiences.</strong>
              After positing your interpretation, continue to observe the subject of your
              speculation to decide whether you want to stick to your original views, or make
              changes. That is one of the great things about an online journal--you can make changes
              to your entries at any time.
            </p>
          </li>
          <li>
            <p>
              <strong>To get thoughts and ideas out of your head.</strong>
              Writing down your thoughts can help relieve pressure or help resolve problems. It will
              also help you focus the task at hand.
            </p>
          </li>
          <li>
            <p>
              <strong>To share your thoughts and ideas with others.</strong>
              Getting opinions from others about what you wrote can help you clarify your feelings
              for a deeper understanding of yourself.
            </p>
          </li>
        </ol>
        <h2>10 Thoughtful and Reflective Journal Prompts</h2>
        <p>
          These are intended to challenge you to think deeper about yourself. Try not to answer in
          simple one sentence responses. Dig deeper into your responses. After every one, ask
          yourself why you chose that answer. Challenge yourself and learn about yourself.
        </p>
        <ol>
          <li>
            <p>
              <strong>Describe a place that feels like home.</strong> What is it about this place
              that feels like home? Think about the location, the time of day, and who is with you.
              Have you felt “at home” anywhere else? What do those places have in common?
            </p>
          </li>
          <li>
            <p>
              <strong>What’s your favorite childhood memory?</strong> Like describing a place that
              feels like home, this is bringing us to a happy place and a happy time. Think of where
              you are, who you are with, and what you are doing. What makes this memory your
              favorite?
            </p>
          </li>
          <li>
            <p>
              <strong>What do you want your tombstone to say?</strong> Thomas Jefferson left
              explicit instructions for his tombstone. It commemorates him for being the author of
              the Declaration of Independence and the Statute of Virginia for religious freedom, and
              the Father of the University of Virginia. The former Secretary of State and President
              of the United States, and that’s what he wanted to be remembered for. So what do you
              want your tombstone to commemorate you for?
            </p>
          </li>
          <li>
            <p>
              <strong>Think of someone you admire.</strong> Describe what qualities they possess
              that you admire. Is this someone you look up to? Or someone you’re jealous of? What
              role do these qualities play in your admiration or jealousy?
            </p>
          </li>
          <li>
            <p>
              <strong>What do you hope people say about you?</strong> We all know that there are
              things we say about others (good and bad) that we just can’t say to that person. For
              some, it’s a deep admiration or even silent love. For others, it could be harsh
              criticism. Most of the time, we won’t know what our impression is upon others. What do
              you hope others say about you when you’re not there? Is that any different from what
              you think they really say about you?
            </p>
          </li>
          <li>
            <p>
              <strong>If today was your last day, how you spend it?</strong> If you only had one
              day, what would you do? Where would you go? Who would you spend it with? Would you run
              to the rooftops and break all the rules? Or would you spend your day just like any
              other day?
            </p>
          </li>
          <li>
            <p>
              <strong>What are you self-conscious about?</strong> We all have little insecurities
              that we try to hide. Some of us are more open about them, and some insecurities are
              much more difficult to hide. What don’t you like about yourself? What are your secret
              (or not so secret) insecurities that you hide? Why do you think you have these
              insecurities? Did you always have that insecurity, or did it grow over time? Why do
              you think that characteristic about yourself is a negative thing?
            </p>
          </li>
          <li>
            <p>
              <strong>What do you like about yourself?</strong> Now that we’ve discussed what you
              don’t like about yourself, remember that there is always someone that admires
              something about you. There is always something about you to be admired. Try to imagine
              what a stranger would think about you from a neutral, unbiased opinion. You have
              qualities to admire, even if you’re self-conscious about yourself. Look at yourself
              like you would anyone else, and realize how amazing you are.
            </p>
          </li>
          <li>
            <p>
              <strong>What’s a compliment that you received recently?</strong>
              Because the previous prompt can be difficult at times. So think about what someone has
              complimented you for. Sometimes, the compliments might also go beyond your looks and
              to your personality. Those are even more endearing, as it shows that you have much
              more to offer beyond your looks. So think hard and think of a compliment someone has
              given you recently.
            </p>
          </li>
          <li>
            <p>
              <strong
                >If you could run any business (and the business would be a guaranteed success),
                what business would you run?</strong
              >
              This will help you rediscover what you truly enjoy, as our jobs are not always what we
              dream of or what we do in our pastimes. But intelligent humans are passionate about
              something, and they make their passions their careers. Not every passion should be a
              career, as working your passion as a job could kill the enjoyment. But this question
              will help you rediscover your passion. What you choose to do with that information is
              up to you.
            </p>
          </li>
        </ol>
